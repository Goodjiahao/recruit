重制说明（为什么重做，而非直接用他补发的文件）：
  1. 成员六原稿把 geo 板块 3 指标塞进单张 38 列宽表 `ads_city_analysis`，不符合
     《标准结果表与接口组件模板》"每人三张表 + ads_<板块>_<指标> 命名"的契约。
  2. 他后续补发的 member6/1.sql~3.sql 中：
       - 1.sql（需求）含全部 21 城，已直接采用；
       - 2.sql / 3.sql（学历 / 经验）**仅含 7 城**（北京~深圳），缺 14 城，不能照搬；
